#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*ptr;

	ptr = NULL;
	while (*s)
	{
		if ((int)*s == c)
			ptr = s;
		s++;
	}
	return ((char *)ptr);
}
